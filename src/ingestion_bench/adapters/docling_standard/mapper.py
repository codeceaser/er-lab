"""Maps a DoclingDocument (docling-core) directly into a validated
ingestion_bench.canonical.CanonicalDocument.

This is the ONLY module besides adapter.py that imports Docling/docling-core
types. Nothing here reads reference_manifest.json or any other benchmark
ground truth -- the mapper's only job is to faithfully represent what
Docling actually reported, never what a benchmark expects it to report.

Docling -> Canonical mapping table (see also the report generated by
scripts/run_docling_standard.py for a live, per-fixture accounting):

    DoclingDocument.pages[page_no]        -> CanonicalUnit
        (PDF/PPTX only -- Docling exposes no page/slide geometry for DOCX;
        see _build_units' docstring for the documented DOCX fallback)
    TitleItem / SectionHeaderItem         -> CanonicalHeading
    TextItem (label TEXT/PARAGRAPH)       -> CanonicalParagraph
    TextItem (label FORMULA/CODE)         -> CanonicalParagraph
        (reduced fidelity: canonical has no dedicated formula/code type;
        recorded as a "reduced_fidelity_mapping" diagnostic)
    TextItem (label PAGE_HEADER/FOOTER/FOOTNOTE) -> skipped ("furniture")
    ListItem                              -> CanonicalListItem
    TextItem referenced by PictureItem.captions -> CanonicalCaption
    TableItem                             -> CanonicalTable / CanonicalTableCell
    PictureItem (with retrievable image bytes)  -> CanonicalPicture
    TextItem whose parent is a PictureItem, and that is NOT that picture's
        caption                            -> OcrAnnotation (derivation=
        "extracted") targeting the picture -- see _map_picture_ocr_children's
        docstring for exactly what evidence this relies on and its limits.
    every other DocItemLabel (DOCUMENT_INDEX, CHECKBOX_*, FORM,
        KEY_VALUE_REGION, GRADING_SCALE, HANDWRITTEN_TEXT, EMPTY_VALUE,
        REFERENCE, FIELD_*, MARKER, CHART as a body item rather than a
        picture) -> skipped ("unsupported_label")

No ImageDescriptionAnnotation, VisualFactAnnotation, SemanticClaimAnnotation,
or model-derived DiagramNode/EdgeAnnotation is ever produced here -- Stage 5A
creates no model-derived content of any kind.
"""

